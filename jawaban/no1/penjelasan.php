<h4>
    Penjelasan singkat
</h4>
<p>
    pada file index.php terdapat sebuah array <b>$arr</b> dengan value seperti pada soal (tidak terurut)
</p>
<p>
    lalu saya melakukan pengurutan <b>$arr</b> pada file urutArray.php dengan membuat sebuah function <b>urut()</b>
</p>
<p>
    pada function <b>urut()</b> saya deklarasikan 2 buah array <b>($huruf, $angka)</b> untuk menampung nilai dari <b>$arr</b> <br>
    kemudian saya menggunakan fungsi <b>sort()</b> pada 2 array tersebut untuk mengurutkan value pada array <br>
    selanjutnya menggunakan fungsi <b>array_merge()</b> unguk menggabungkan kembali <b>($huruf, $angka)</b> dan disimpan pada sebuah variable <b>$urutArr</b>
</p>